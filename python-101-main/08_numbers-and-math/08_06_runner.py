# If a runner runs 10 miles in 30 minutes and 30 seconds,
# What is their average speed in kilometers per hour?
# (Tip: 1 mile = 1.6 km)


runner = "Speedy Max"
distance_in_miles = 10
distance_in_km = distance_in_miles * 1.6
time_in_minutes = 30.5
average_speed = distance_in_km / time_in_minutes

print(average_speed)
print(f"In average, {runner} runs at {round(average_speed,2)} km/h")